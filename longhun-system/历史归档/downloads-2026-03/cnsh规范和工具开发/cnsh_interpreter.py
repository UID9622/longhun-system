# 第二部分：中文关键字映射
# ============================================================

class CNSH关键字:
    """
    CNSH的中文关键字
    映射到Python的关键字（内部实现）
    """
    
    关键字映射 = {
        # 基本关键字
        "如果": "if",
        "否则如果": "elif",
        "否则": "else",
        "循环": "for",
        "当": "while",
        "中断": "break",
        "继续": "continue",
        "返回": "return",
        "函数": "def",
        "类": "class",
        "导入": "import",
        "从": "from",
        "尝试": "try",
        "捕获": "except",
        "最终": "finally",
        "抛出": "raise",
        "通过": "pass",
        
        # 数据类型
        "真": "True",
        "假": "False",
        "空": "None",
        "并且": "and",
        "或者": "or",
        "不是": "not",
        "在": "in",
        "是": "is",
        
        # 特殊关键字
        "变量": "",  # 不需要映射
        "常量": "",
        "打印": "print",
        "输入": "input",
        "长度": "len",
        "范围": "range",
        "类型": "type",
        "字符串": "str",
        "整数": "int",
        "小数": "float",
        "列表": "list",
        "字典": "dict",
        "集合": "set",
        
        # OOP关键字
        "自己": "self",
        "初始化": "__init__",
        "方法": "def",
    }
    
    @classmethod
    def 转换为Python(cls, 中文代码: str) -> str:
        """
        将CNSH中文代码转换为Python代码
        保留变量名（包括文化关键词）
        """
        Python代码 = 中文代码
        
        # 按长度排序，先替换长的关键字（避免"否则如果"被"否则"替换）
        for 中文, 英文 in sorted(
            cls.关键字映射.items(),
            key=lambda x: len(x[0]),
            reverse=True
        ):
            if 英文:  # 如果有映射
                # 使用正则表达式，只替换独立的关键字
                pattern = r'\b' + re.escape(中文) + r'\b'
                Python代码 = re.sub(pattern, 英文, Python代码)
        
        return Python代码


