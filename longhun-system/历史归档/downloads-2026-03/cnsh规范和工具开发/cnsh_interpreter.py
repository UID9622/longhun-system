# 第三部分：CNSH解释器
# ============================================================

class CNSH解释器:
    """
    CNSH中文编程语言解释器
    
    工作流程:
      1. 读取.cnsh文件
      2. 检查文化关键词是否被翻译
      3. 将中文关键字转换为Python
      4. 保留变量名（包括文化关键词）
      5. 执行代码
    """
    
    def __init__(self):
        self.文化守护者 = 文化关键词守护者()
        self.全局命名空间 = {}
    
    def 读取文件(self, 文件路径: str) -> str:
        """读取CNSH源文件"""
        try:
            with open(文件路径, 'r', encoding='utf-8') as f:
                return f.read()
        except FileNotFoundError:
            print(f"❌ 错误: 找不到文件 '{文件路径}'")
            sys.exit(1)
        except Exception as e:
            print(f"❌ 错误: 读取文件失败 - {e}")
            sys.exit(1)
    
    def 检查文化主权(self, 代码: str) -> bool:
        """
        检查代码是否违反文化主权规则
        
        返回:
            True = 通过检查
            False = 发现违规
        """
        错误列表 = self.文化守护者.检查代码(代码)
        
        if 错误列表:
            print("\n🚨 文化主权检查失败:")
            for 错误 in 错误列表:
                print(f"  {错误}")
            print("\n💡 提示: CNSH保护文化关键词不被翻译")
            print("   这是文化主权，是尊严！\n")
            return False
        
        return True
    
    def 转换代码(self, CNSH代码: str) -> str:
        """
        将CNSH代码转换为可执行的Python代码
        保留所有变量名（包括文化关键词）
        """
        return CNSH关键字.转换为Python(CNSH代码)
    
    def 执行(self, 文件路径: str):
        """执行CNSH文件"""
        # 读取代码
        CNSH代码 = self.读取文件(文件路径)
        
        # 检查文化主权
        if not self.检查文化主权(CNSH代码):
            sys.exit(1)
        
        # 转换代码
        Python代码 = self.转换代码(CNSH代码)
        
        # 打印DNA追溯信息
        print("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━")
        print("CNSH中文编程语言 v1.0")
        print("DNA追溯码: #龍芯⚡️2026-03-10-CNSH-v1.0")
        print("创建者: UID9622 诸葛鑫")
        print("理论指导: 曾仕强老师")
        print("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n")
        
        # 执行代码
        try:
            exec(Python代码, self.全局命名空间)
        except Exception as e:
            print(f"\n❌ 运行错误: {e}")
            import traceback
            traceback.print_exc()
            sys.exit(1)


