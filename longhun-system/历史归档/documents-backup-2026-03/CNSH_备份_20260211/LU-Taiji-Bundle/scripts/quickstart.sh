#!/bin/bash
# ============================================================================
# LU-Taiji Bundle 快速启动脚本
# ============================================================================
# 功能：一键初始化 LU-Taiji Bundle，包括验证、校验、权限设置和配置
# DNA: #ZHUGEXIN⚡️2025-01-27-LU-TAIJI-QUICKSTART-v2.1
# 确认码: #CONFIRM🌌9622-ONLY-ONCE🧬LK9X-772Z
# ============================================================================

set -e  # 遇到错误立即退出

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BUNDLE_ROOT="$(dirname "$SCRIPT_DIR")"

# ============================================================================
# ANSI 颜色定义
# ============================================================================
GREEN='\033[0;32m'   # 绿色 - 成功
RED='\033[0;31m'     # 红色 - 错误
YELLOW='\033[0;33m' # 黄色 - 警告
BLUE='\033[0;34m'    # 蓝色 - 信息
NC='\033[0m'         # 重置颜色

# ============================================================================
# 显示横幅
# ============================================================================
banner() {
    echo -e "${BLUE}"
    echo "╔═══════════════════════════════════════════════╗"
    echo "║        LU-Taiji Bundle v2.1                  ║"
    echo "║   Quick Start & Verification Tool             ║"
    echo "╚═══════════════════════════════════════════════╝"
    echo -e "${NC}"
    echo -e "DNA: #ZHUGEXIN⚡️2025-01-27-LU-TAIJI-BUNDLE-v2.1"
    echo ""
}

# ============================================================================
# 步骤 1: 验证 Schema
# ============================================================================
validate() {
    echo -e "${YELLOW}═══════════════════════════════════════${NC}"
    echo -e "${YELLOW}步骤 1: Schema 验证${NC}"
    echo -e "${YELLOW}═══════════════════════════════════════${NC}\n"

    if ! node "$SCRIPT_DIR/validate_schema.mjs"; then
        echo -e "${RED}✗ 验证失败！${NC}"
        return 1
    fi
    echo ""
}

# ============================================================================
# 步骤 2: 生成校验和
# ============================================================================
checksum() {
    echo -e "${YELLOW}═══════════════════════════════════════${NC}"
    echo -e "${YELLOW}步骤 2: 校验和生成${NC}"
    echo -e "${YELLOW}═══════════════════════════════════════${NC}\n"

    if ! bash "$SCRIPT_DIR/checksum.sh"; then
        echo -e "${RED}✗ 校验和生成失败！${NC}"
        return 1
    fi
    echo ""
}

# ============================================================================
# 步骤 3: 设置脚本执行权限
# ============================================================================
set_permissions() {
    echo -e "${YELLOW}═══════════════════════════════════════${NC}"
    echo -e "${YELLOW}步骤 3: 设置执行权限${NC}"
    echo -e "${YELLOW}═══════════════════════════════════════${NC}\n"

    chmod +x "$SCRIPT_DIR"/*.sh   # 所有 shell 脚本
    chmod +x "$SCRIPT_DIR"/*.mjs  # 所有 Node.js 脚本

    echo -e "${GREEN}✓ 权限设置完成${NC}"
    echo ""
}

# ============================================================================
# 步骤 4: 配置向导（生成 .env 模板）
# ============================================================================
configure() {
    echo -e "${YELLOW}═══════════════════════════════════════${NC}"
    echo -e "${YELLOW}步骤 4: 配置${NC}"
    echo -e "${YELLOW}═══════════════════════════════════════${NC}\n"

    if [ -f "$BUNDLE_ROOT/.env" ]; then
        echo -e "${GREEN}✓ .env 文件已存在${NC}"
        echo -e "${YELLOW}  如需更新，请编辑 $BUNDLE_ROOT/.env${NC}"
    else
        echo -e "${YELLOW}⚠️  正在创建 .env 模板...${NC}"
        cat > "$BUNDLE_ROOT/.env" << 'EOF'
# ============================================================================
# LU-Taiji Bundle 配置文件
# ============================================================================
# 请从以下地址获取 Notion API Key: https://www.notion.so/my-integrations
# ============================================================================
NOTION_API_KEY=your_integration_token_here

# 从 Notion 数据库 URL 中提取 Database ID
# ============================================================================
NOTION_DATABASE_ID=your_database_id_here

# 可选: 自定义用户名
# ============================================================================
CUSTOM_USER_NAME=Lucky

# 可选: 调试模式 (true/false)
# ============================================================================
DEBUG_MODE=false
EOF
        echo -e "${GREEN}✓ .env 模板创建完成${NC}"
        echo -e "${YELLOW}  ⚠️  请编辑 $BUNDLE_ROOT/.env 填入你的凭证${NC}"
    fi
    echo ""
}

# ============================================================================
# 步骤 5: 显示总结
# ============================================================================
summary() {
    echo -e "${BLUE}═══════════════════════════════════════${NC}"
    echo -e "${BLUE}✓ Bundle 设置完成！${NC}"
    echo -e "${BLUE}═══════════════════════════════════════${NC}\n"

    echo -e "下一步操作："
    echo -e "  1. 编辑 ${GREEN}.env${NC} 填入你的 Notion 凭证"
    echo -e "  2. 运行: ${GREEN}node scripts/validate_schema.mjs${NC} (验证 schema)"
    echo -e "  3. 运行: ${GREEN}bash scripts/checksum.sh${NC} (生成校验和)"
    echo -e "  4. 运行: ${GREEN}node scripts/import_notion.mjs${NC} (导入到 Notion)"
    echo ""
    echo -e "文档："
    echo -e "  - 查看 ${GREEN}docs/README.md${NC} 获取详细说明"
    echo ""
}

# ============================================================================
# 主函数
# ============================================================================
main() {
    banner

    echo -e "${BLUE}开始 LU-Taiji Bundle 设置...${NC}\n"

    validate || exit 1
    checksum || exit 1
    set_permissions || exit 1
    configure || exit 1

    summary

    echo -e "${GREEN}═══════════════════════════════════════${NC}"
    echo -e "${GREEN}  ✓ 所有检查通过！${NC}"
    echo -e "${GREEN}═══════════════════════════════════════${NC}"
}

# ============================================================================
# 执行主函数
# ============================================================================
main "$@"
