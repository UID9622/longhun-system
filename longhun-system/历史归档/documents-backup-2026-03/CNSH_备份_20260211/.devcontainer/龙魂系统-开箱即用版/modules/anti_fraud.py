    def __init__(self):
        self.检测次数 = 0
        self.阻断次数 = 0
    
    def 检测文本(self, 文本: str) -> 反诈检测结果:
        """
        检测文本是否包含诈骗关键词
        
        Args:
            文本: 用户输入的文本
            
        Returns:
            反诈检测结果
        """
        self.检测次数 += 1
        
        if not 文本 or len(文本.strip()) == 0:
            return 反诈检测结果(
                是否诈骗=False,
                诈骗类型=None,
                严重程度="无",
                匹配关键词=[],
                教育内容="请输入要检测的内容",
                建议操作="继续输入",
                dna追溯码=self._生成DNA("空检测")
            )
        
        # 遍历所有诈骗类型
        for 类型, 数据 in self.诈骗数据库.items():
            匹配词 = []
            for 关键词 in 数据["keywords"]:
                if 关键词 in 文本:
                    匹配词.append(关键词)
            
            if 匹配词:
                self.阻断次数 += 1
                return 反诈检测结果(
                    是否诈骗=True,
                    诈骗类型=类型,
                    严重程度=数据["severity"],
                    匹配关键词=匹配词,
                    教育内容=数据["教育"],
                    建议操作="立即停止操作，挂断电话，如有疑问拨打110",
                    dna追溯码=self._生成DNA(f"检测-{类型}")
                )
        
        # 未检测到诈骗
        return 反诈检测结果(
            是否诈骗=False,
            诈骗类型=None,
            严重程度="安全",
            匹配关键词=[],
            教育内容="✅ 未检测到诈骗关键词\n\n但请注意：诈骗手段不断更新，请保持警惕。",
            建议操作="继续操作",
            dna追溯码=self._生成DNA("检测-安全")
        )
    
    def 批量检测(self, 文本列表: List[str]) -> List[反诈检测结果]:
        """批量检测多个文本"""
        return [self.检测文本(文本) for 文本 in 文本列表]
    
    def 获取统计(self) -> Dict:
        """获取反诈守护统计"""
        return {
            "总检测次数": self.检测次数,
            "阻断次数": self.阻断次数,
            "安全率": f"{((self.检测次数 - self.阻断次数) / self.检测次数 * 100):.1f}%" if self.检测次数 > 0 else "N/A"
        }
    
    def _生成DNA(self, 操作: str) -> str:
        """生成DNA追溯码"""
        from datetime import datetime
        日期 = datetime.now().strftime("%Y-%m-%d")
        return f"#龍芯⚡️{日期}-反诈-{操作}-v1.0"


