# 四、DNA 自动生成器
# ══════════════════════════════════════════════════════════

def 生成DNA(操作名称: str, 层级="L4") -> str:
    """L3=日常操作  L4=实时执行（最常用）"""
    日期 = date.today().strftime("%Y-%m-%d")
    哈希 = hashlib.md5(操作名称.encode()).hexdigest()[:8].upper()
    if 层级 == "L3":
        return f"#龍芯⚡️{日期}-{操作名称[:10].replace(' ','-')}-{哈希}"
    return f"#龍芯⚡️{日期}-{操作名称[:8].replace(' ','-')}-EXEC-{哈希[:4]}"


