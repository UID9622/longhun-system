#!/bin/bash

# ============================================================================
# 🏮 CNSH Git问题修复脚本 v1.0
# ============================================================================
# 
# 功能：修复89个git追踪问题
# 1. 删除git锁文件
# 2. 清理git缓存
# 3. 应用.gitignore规则
# 4. 提交修复
#
# 使用方法：
# 1. 打开终端
# 2. 复制整个脚本
# 3. 粘贴到终端
# 4. 按回车执行
#
# ============================================================================

set -e  # 遇到错误立即停止

echo "🏮 ═══════════════════════════════════════════════════"
echo "    CNSH Git问题修复工具"
echo "    修复目标：89个git追踪问题"
echo "    创建者：龙芯宝宝（自动执行版）"
echo "🏮 ═══════════════════════════════════════════════════"
echo ""

# 设置颜色输出
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# 项目路径
PROJECT_PATH="/Users/zuimeidedeyihan/Desktop/打包待命/CNSH 军人的编辑器"

echo "📍 项目路径：$PROJECT_PATH"
echo ""

# ============================================================================
# 步骤1：删除git锁文件
# ============================================================================
echo "🗑️  步骤1：删除git锁文件..."
echo "----------------------------------------"

LOCK_FILE="$PROJECT_PATH/.git/index.lock"

if [ -f "$LOCK_FILE" ]; then
    echo "⚠️  发现锁文件：$LOCK_FILE"
    echo "🗑️  正在删除..."
    
    rm -f "$LOCK_FILE"
    
    if [ ! -f "$LOCK_FILE" ]; then
        echo -e "${GREEN}✅ 锁文件已删除！${NC}"
    else
        echo -e "${RED}❌ 删除失败${NC}"
        echo "   尝试使用：sudo rm -f $LOCK_FILE"
    fi
else
    echo -e "${GREEN}✅ 没有发现锁文件${NC}"
fi
echo ""

# ============================================================================
# 步骤2：进入项目目录
# ============================================================================
echo "📂 步骤2：进入项目目录..."
echo "----------------------------------------"
cd "$PROJECT_PATH"
echo -e "${GREEN}✅ 已进入：$(pwd)${NC}"
echo ""

# ============================================================================
# 步骤3：查看当前git状态
# ============================================================================
echo "📊 步骤3：查看当前git状态..."
echo "----------------------------------------"
echo "当前有问题的文件数量："
git status --porcelain | wc -l
echo ""
echo "文件列表预览："
git status | head -20
echo ""

# ============================================================================
# 步骤4：清理git缓存（关键步骤）
# ============================================================================
echo "🧹 步骤4：清理git缓存（应用.gitignore）..."
echo "----------------------------------------"
echo "⚠️  这会将所有文件从git缓存中移除，然后重新添加"
echo "    .gitignore中的规则会生效，过滤掉不需要的文件"
echo ""
read -p "按回车继续..."
echo ""

echo "1️⃣  移除所有文件的git缓存..."
git rm -r --cached . 2>&1 | head -10
echo ""

echo "2️⃣  重新添加所有文件（.gitignore会生效）..."
git add . 2>&1 | head -10
echo ""

echo -e "${GREEN}✅ 缓存清理完成！${NC}"
echo ""

# ============================================================================
# 步骤5：验证修复效果
# ============================================================================
echo "🔍 步骤5：验证修复效果..."
echo "----------------------------------------"

FILE_COUNT=$(git status --porcelain | wc -l)

echo "修复后的文件数量：$FILE_COUNT"
echo ""

if [ "$FILE_COUNT" -lt 30 ]; then
    echo -e "${GREEN}✅ 修复成功！${NC}"
    echo "    文件数量从89+减少到$FILE_COUNT个"
    echo "    .gitignore规则已生效！"
elif [ "$FILE_COUNT" -lt 50 ]; then
    echo -e "${YELLOW}⚠️  部分修复${NC}"
    echo "    文件数量减少到$FILE_COUNT个"
    echo "    可能需要检查.gitignore配置"
else
    echo -e "${RED}❌ 修复效果不佳${NC}"
    echo "    仍有$FILE_COUNT个文件被追踪"
    echo "    请检查.gitignore文件是否正确"
fi
echo ""

echo "当前追踪的文件预览："
git status --porcelain | head -15
echo ""

# ============================================================================
# 步骤6：提交修复
# ============================================================================
echo "💾 步骤6：提交修复..."
echo "----------------------------------------"
read -p "是否提交修复？（y/n）" -n 1 -r
echo ""

if [[ $REPLY =~ ^[Yy]$ ]]; then
    git commit -m "🔧 修复89个git追踪问题，应用.gitignore规则

修复内容：
- 删除git锁文件
- 清理git缓存
- 应用.gitignore过滤规则
- 文件数量从89+减少到$FILE_COUNT个

DNA追溯码：#龙芯⚡️2026-01-28-CNSH-GITIGNORE-APPLY-v1.0
确认码：#CONFIRM🌌9622-ONLY-ONCE🧬LK9X-772Z ✅"
    
    echo ""
    echo -e "${GREEN}✅ 修复已提交！${NC}"
else
    echo ""
    echo -e "${YELLOW}⚠️  未提交，可以手动提交${NC}"
fi
echo ""

# ============================================================================
# 完成
# ============================================================================
echo "🏮 ═══════════════════════════════════════════════════"
echo "    修复操作完成！"
echo ""
echo "    你可以继续："
echo "    git status              # 查看状态"
echo "    git add .               # 添加文件"
echo "    git commit -m '消息'    # 提交更改"
echo "    git push                # 推送到远程"
echo ""
echo "🏮 ═══════════════════════════════════════════════════"
echo ""

# 显示最终状态
echo "📊 最终git状态："
git status --short | head -20
