def 易经推演(北京时间: datetime.datetime) -> 八卦:
    """
    根据北京时间推演当前卦象
    
    十二时辰对应卦象：
    子时(23-01) → 坎卦  丑时(01-03) → 坤卦
    寅时(03-05) → 震卦  卯时(05-07) → 巽卦
    辰时(07-09) → 兑卦  巳时(09-11) → 离卦
    午时(11-13) → 乾卦  未时(13-15) → 坤卦
    申时(15-17) → 兑卦  酉时(17-19) → 巽卦
    戌时(19-21) → 艮卦  亥时(21-23) → 坎卦
    """
    时辰卦象映射 = [
        (23, 1, 八卦.坎卦),   # 子时
        (1, 3, 八卦.坤卦),    # 丑时
        (3, 5, 八卦.震卦),    # 寅时
        (5, 7, 八卦.巽卦),    # 卯时
        (7, 9, 八卦.兑卦),    # 辰时
        (9, 11, 八卦.离卦),   # 巳时
        (11, 13, 八卦.乾卦),  # 午时
        (13, 15, 八卦.坤卦),  # 未时
        (15, 17, 八卦.兑卦),  # 申时
        (17, 19, 八卦.巽卦),  # 酉时
        (19, 21, 八卦.艮卦),  # 戌时
        (21, 23, 八卦.坎卦),  # 亥时
    ]
    
    当前小时 = 北京时间.hour
    
    for 开始, 结束, 卦象 in 时辰卦象映射:
        if 开始 <= 当前小时 < 结束:
            return 卦象
        # 处理跨夜的子时
        if 开始 == 23 and 当前小时 >= 23:
            return 卦象
        if 结束 == 1 and 当前小时 < 1:
            return 卦象
    
    return 八卦.乾卦  # 默认返回乾卦


