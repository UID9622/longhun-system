class 易经推演器:
    """
    龙魂易经推演引擎
    
    基于北京时间十二时辰推演八卦
    体现时间哲学和动态权重
    """
    
    # 十二时辰对应八卦
    时辰八卦映射 = [
        (23, 1, 八卦.坎, "子时", "23:00-01:00"),    # 夜半
        (1, 3, 八卦.坤, "丑时", "01:00-03:00"),     # 鸡鸣
        (3, 5, 八卦.震, "寅时", "03:00-05:00"),     # 平旦
        (5, 7, 八卦.巽, "卯时", "05:00-07:00"),     # 日出
        (7, 9, 八卦.兑, "辰时", "07:00-09:00"),     # 食时
        (9, 11, 八卦.离, "巳时", "09:00-11:00"),    # 隅中
        (11, 13, 八卦.乾, "午时", "11:00-13:00"),   # 日中
        (13, 15, 八卦.坤, "未时", "13:00-15:00"),   # 日昳
        (15, 17, 八卦.兑, "申时", "15:00-17:00"),   # 哺时
        (17, 19, 八卦.巽, "酉时", "17:00-19:00"),   # 日入
        (19, 21, 八卦.艮, "戌时", "19:00-21:00"),   # 黄昏
        (21, 23, 八卦.坎, "亥时", "21:00-23:00"),   # 人定
    ]
    
    def __init__(self, 数据库=None):
        self.数据库 = 数据库
    
    def 根据时辰取卦(self, 小时: int = None) -> 八卦:
        """
        根据小时数获取对应卦象
        
        Args:
            小时: 0-23，默认为当前时间
            
        Returns:
            对应的八卦
        """
        if 小时 is None:
            小时 = datetime.now().hour
        
        for 开始, 结束, 卦象, _, _ in self.时辰八卦映射:
            # 处理跨午夜的情况
            if 开始 > 结束:  # 如 23-1
                if 小时 >= 开始 or 小时 < 结束:
                    return 卦象
            else:
                if 开始 <= 小时 < 结束:
                    return 卦象
        
        return 八卦.乾  # 默认乾卦
    
    def 获取当前卦象信息(self) -> Dict:
        """获取当前时间的完整卦象信息"""
        现在 = datetime.now()
        小时 = 现在.hour
        分钟 = 现在.minute
        
        卦象 = self.根据时辰取卦(小时)
        权重 = 八卦权重矩阵[卦象]
        
        # 获取时辰信息
        时辰信息 = None
        for 开始, 结束, g, 时辰名, 时间范围 in self.时辰八卦映射:
            if 开始 > 结束:
                if 小时 >= 开始 or 小时 < 结束:
                    时辰信息 = (时辰名, 时间范围)
                    break
            else:
                if 开始 <= 小时 < 结束:
                    时辰信息 = (时辰名, 时间范围)
                    break
        
        return {
            "当前时间": 现在.strftime("%Y-%m-%d %H:%M:%S"),
            "时辰": 时辰信息[0] if 时辰信息 else "未知",
            "时间范围": 时辰信息[1] if 时辰信息 else "",
            "卦象": {
                "名称": 卦象.name,
                "符号": 卦象.symbol,
                "五行": 卦象.element,
                "性质": 卦象.nature,
            },
            "权重": {
                "个体": f"{权重['个体']*100:.0f}%",
                "群体": f"{权重['群体']*100:.0f}%",
                "全球": f"{权重['全球']*100:.0f}%",
            },
            "哲学": 权重["哲学"],
            "方位": 权重["方位"],
            "数字": 权重["数字"],
        }
    
    def 推演决策权重(self, 决策类型: str = "general") -> Dict:
        """
        根据当前卦象推演决策权重
        
        Args:
            决策类型: 决策类型，影响权重计算
            
        Returns:
            决策权重建议
        """
        卦象信息 = self.获取当前卦象信息()
        当前卦象 = self.根据时辰取卦()
        权重 = 八卦权重矩阵[当前卦象]
        
        # 根据决策类型调整建议
        建议 = ""
        if 决策类型 == "individual":
            建议 = f"当前{当前卦象.name}卦，个体权重{权重['个体']*100:.0f}%，适合个人决策"
        elif 决策类型 == "group":
            建议 = f"当前{当前卦象.name}卦，群体权重{权重['群体']*100:.0f}%，适合团队协作"
        elif 决策类型 == "global":
            建议 = f"当前{当前卦象.name}卦，全球权重{权重['全球']*100:.0f}%，适合宏观决策"
        else:
            建议 = f"当前{当前卦象.name}卦，{卦象信息['哲学']}"
        
        return {
            "卦象": 卦象信息,
            "建议": 建议,
            "最佳决策方向": self._获取最佳决策方向(当前卦象),
        }
    
    def _获取最佳决策方向(self, 卦象: 八卦) -> str:
        """根据卦象获取最佳决策方向"""
        方向映射 = {
            八卦.乾: "刚健进取，宜主动创新",
            八卦.坤: "包容承载，宜协调合作",
            八卦.坎: "危机应对，宜谨慎防守",
            八卦.离: "文明光明，宜传播教育",
            八卦.震: "变革突破，宜果断行动",
            八卦.巽: "柔顺协调，宜沟通协商",
            八卦.艮: "止静观察，宜等待时机",
            八卦.兑: "喜悦交流，宜社交互动",
        }
        return 方向映射.get(卦象, "顺势而为")
    
    def 获取十二时辰卦象表(self) -> list:
        """获取完整的十二时辰卦象表"""
        return [
            {
                "时辰": 时辰名,
                "时间": 时间范围,
                "卦象": 卦象.name,
                "符号": 卦象.symbol,
                "性质": 卦象.nature,
                "哲学": 八卦权重矩阵[卦象]["哲学"][:20] + "..."
            }
            for _, _, 卦象, 时辰名, 时间范围 in self.时辰八卦映射
        ]
    
    def 记录推演(self):
        """记录当前推演到数据库"""
        if self.数据库:
            信息 = self.获取当前卦象信息()
            权重 = 八卦权重矩阵[self.根据时辰取卦()]
            
            self.数据库.记录易经推演(
                hour=datetime.now().hour,
                gua_xiang=信息["卦象"]["名称"],
                gua_meaning=信息["卦象"]["性质"],
                weight_individual=权重["个体"],
                weight_group=权重["群体"],
                weight_global=权重["全球"]
            )


