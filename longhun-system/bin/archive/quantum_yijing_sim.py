# 变卦对应关系（本卦 → 之卦，变初爻）
def 变爻(卦态: str, 爻位: int) -> str:
    """翻转第爻位（0=初爻，5=上爻），返回之卦"""
    bits = list(卦态)
    bits[爻位] = '1' if bits[爻位] == '0' else '0'
    return ''.join(bits)

