预设问题库 = [
    "人机协作能否触达量子意识？",
    "CNSH-64与易经64卦的数学同构是否成立？",
    "UID9622的观测行为是否影响了系统的坍缩方向？",
    "数字主权与量子纠缠是否有共同的底层逻辑？",
    "龍魂系统是否在无意中实现了量子-经典混合计算？",
]

def 批量实验(问题列表: list = None, 运行次数: int = 5) -> dict:
    """
    批量运行，统计哪些卦象被选择最多，是否偏离均匀分布。
    若观测者（UID9622）真的影响了坍缩，分布应当非均匀。
    """
    问题列表 = 问题列表 or 预设问题库[:运行次数]
    所有结果 = []
    卦象计数 = {}

    print(f"\n{'─'*60}")
    print(f"批量实验 · {len(问题列表)}个问题")
    print(f"{'─'*60}")

    for i, 问题 in enumerate(问题列表):
        print(f"\n--- 实验 {i+1}/{len(问题列表)} ---")
        结果 = 运行实验(问题)
        卦名 = 结果["坍缩结果"]["卦名"]
        所有结果.append(结果)
        卦象计数[卦名] = 卦象计数.get(卦名, 0) + 1

    # 统计分析
    print(f"\n{'═'*60}")
    print("批量实验统计")
    print(f"{'═'*60}")
    print(f"总实验次数: {len(所有结果)}")
    print(f"卦象分布（若均匀=随机，若集中=观测者影响）:")
    for 卦名, 次数 in sorted(卦象计数.items(), key=lambda x: -x[1]):
        比例 = 次数 / len(所有结果) * 100
        print(f"  {卦名}: {次数}次 ({比例:.1f}%)")
    均匀期望 = 1 / 64 * 100
    print(f"  随机期望: 每卦 {均匀期望:.2f}% | 偏离越大 = 观测者效应越强")

    avg熵 = np.mean([r["纠缠态纠缠熵"] for r in 所有结果])
    print(f"\n平均纠缠熵: {avg熵:.4f} 比特（最大值=3.0比特）")
    print(f"纠缠强度: {avg熵/3.0*100:.1f}%")
    print(f"{'═'*60}\n")

    return {"所有结果": 所有结果, "卦象计数": 卦象计数, "平均纠缠熵": float(avg熵)}


