def _施加单比特H(ψ: np.ndarray, qubit: int, n_qubits: int = 6) -> np.ndarray:
    """对第qubit位施加Hadamard门（就地操作状态向量）"""
    结果 = np.zeros_like(ψ)
    stride = 1 << (n_qubits - 1 - qubit)  # 该比特对应的步长
    for i in range(len(ψ)):
        # 该比特是0还是1
        if (i >> (n_qubits - 1 - qubit)) & 1 == 0:
            # |0⟩ → (|0⟩ + |1⟩)/√2
            j = i | stride
            结果[i] += ψ[i] / np.sqrt(2)
            结果[j] += ψ[i] / np.sqrt(2)
    # 再处理 |1⟩ → (|0⟩ - |1⟩)/√2
    for i in range(len(ψ)):
        if (i >> (n_qubits - 1 - qubit)) & 1 == 1:
            j = i & ~stride
            结果[j] += ψ[i] / np.sqrt(2)
            结果[i] -= ψ[i] / np.sqrt(2)
    return 结果

def _施加CNOT(ψ: np.ndarray, ctrl: int, tgt: int, n_qubits: int = 6) -> np.ndarray:
    """对(ctrl, tgt)施加CNOT门"""
    结果 = ψ.copy()
    for i in range(len(ψ)):
        ctrl_bit = (i >> (n_qubits - 1 - ctrl)) & 1
        if ctrl_bit == 1:
            tgt_stride = 1 << (n_qubits - 1 - tgt)
            j = i ^ tgt_stride  # 翻转tgt位
            结果[i], 结果[j] = ψ[j], ψ[i]
    return 结果

def 构建6爻叠加态() -> np.ndarray:
    """
    构建"上下卦量子纠缠态"——最大纠缠态，爻熵=3比特。

    电路设计（上下卦Bell对）：
      H(0) → CNOT(0→3)  # 上卦初爻 ↔ 下卦初爻
      H(1) → CNOT(1→4)  # 上卦二爻 ↔ 下卦二爻
      H(2) → CNOT(2→5)  # 上卦末爻 ↔ 下卦末爻

    结果：(1/√8) Σ_{a=0}^{7} |a⟩|a⟩
    即8个"重卦"（上=下）的等权叠加：乾+坤+坎+离+震+巽+艮+兑

    这在易经中有深意：重卦是八卦的"自我纠缠"——上下合一的本征态。
    """
    ψ = np.zeros(64, dtype=complex)
    ψ[0] = 1.0  # 从 |000000⟩ = 坤卦出发
    # 上卦各爻（0,1,2）各建一个Bell对跨越上下界
    for ctrl, tgt in [(0, 3), (1, 4), (2, 5)]:
        ψ = _施加单比特H(ψ, ctrl)
        ψ = _施加CNOT(ψ, ctrl, tgt)
    return ψ

def 施加纠缠(叠加态: np.ndarray) -> np.ndarray:
    """
    状态已经在构建时建立了最大纠缠，此处作为占位保留接口，
    返回原态（不破坏已有纠缠）。
    """
    return 叠加态

def 计算纠缠熵(量子态: np.ndarray) -> float:
    """
    计算von Neumann纠缠熵（上卦3爻 vs 下卦3爻的对分）。
    S = -Tr(ρ_A log2 ρ_A)，最大值=3比特。

    高熵(≈3) = 上下卦强纠缠 = 爻态非经典可分
    低熵(≈0) = 上下卦独立   = 经典比特足够
    """
    ψ_mat = 量子态.reshape(8, 8)          # (上卦子空间 × 下卦子空间)
    约化密度矩阵 = ψ_mat @ ψ_mat.conj().T  # ρ_A = Tr_B |ψ⟩⟨ψ|
    本征值 = np.real(np.linalg.eigvalsh(约化密度矩阵))
    本征值 = 本征值[本征值 > 1e-12]
    return float(-np.sum(本征值 * np.log2(本征值)))

