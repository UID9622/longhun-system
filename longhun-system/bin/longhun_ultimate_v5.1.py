class 情緒時間線:
    """
    情緒時間線追蹤
    用於動態調整決策參數
    """
    
    def __init__(self):
        self._情緒記錄 = []
        self._當前情緒 = 0.5  # 0-1, 0.5中性
    
    def 記錄情緒(self, 情緒: str, 強度: float = 0.5) -> None:
        """記錄情緒事件"""
        情緒映射 = {
            "平靜": 0.3, "愉悅": 0.6, "興奮": 0.8, "憤怒": 0.9,
            "悲傷": 0.2, "焦慮": 0.7, "恐懼": 0.1, "希望": 0.6
        }
        基值 = 情緒映射.get(情緒, 0.5)
        值 = 基值 * (0.5 + 強度 * 0.5)
        
        self._情緒記錄.append({
            "時間": datetime.now().isoformat(),
            "情緒": 情緒,
            "強度": 強度,
            "值": 值
        })
        
        # 更新當前情緒（移動平均）
        if len(self._情緒記錄) > 10:
            最近值 = [r["值"] for r in self._情緒記錄[-10:]]
            self._當前情緒 = sum(最近值) / len(最近值)
        else:
            self._當前情緒 = (self._當前情緒 + 值) / 2
    
    def 獲取當前情緒因子(self) -> float:
        """獲取當前情緒因子（用於三才流場修正）"""
        return self._當前情緒
    
    def 獲取趨勢(self) -> str:
        """獲取情緒趨勢"""
        if len(self._情緒記錄) < 3:
            return "平穩"
        
        最近三個 = [r["值"] for r in self._情緒記錄[-3:]]
        if 最近三個[-1] > 最近三個[0] * 1.1:
            return "上升"
        elif 最近三個[-1] < 最近三個[0] * 0.9:
            return "下降"
        return "平穩"


