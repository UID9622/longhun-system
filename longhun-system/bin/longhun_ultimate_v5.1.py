class 三才流場引擎:
    """
    三才流場算法（天地人）
    用於動態權重計算和場景演化
    """
    
    def __init__(self):
        self.天場 = 0.35
        self.地場 = 0.15
        self.人場 = 0.50
        self._歷史記錄 = []
    
    def 更新權重(self, 卦象: str, 情緒因子: float = 0.5) -> Dict:
        """根據卦象和情緒因子更新三才權重"""
        卦象權重 = {
            "乾": {"天": 0.6, "地": 0.2, "人": 0.2},
            "坤": {"天": 0.2, "地": 0.6, "人": 0.2},
            "坎": {"天": 0.1, "地": 0.3, "人": 0.6},
            "離": {"天": 0.4, "地": 0.3, "人": 0.3},
            "震": {"天": 0.5, "地": 0.2, "人": 0.3},
            "巽": {"天": 0.3, "地": 0.4, "人": 0.3},
            "艮": {"天": 0.3, "地": 0.5, "人": 0.2},
            "兌": {"天": 0.3, "地": 0.3, "人": 0.4},
        }
        
        基礎權重 = 卦象權重.get(卦象, {"天": 0.35, "地": 0.15, "人": 0.50})
        
        # 情緒修正
        情緒偏移 = (情緒因子 - 0.5) * 0.2
        self.天場 = max(0.05, min(0.8, 基礎權重["天"] + 情緒偏移))
        self.地場 = max(0.05, min(0.8, 基礎權重["地"] - 情緒偏移 * 0.5))
        self.人場 = max(0.1, min(0.8, 基礎權重["人"] + 情緒偏移 * 0.5))
        
        # 歸一化
        總和 = self.天場 + self.地場 + self.人場
        self.天場 /= 總和
        self.地場 /= 總和
        self.人場 /= 總和
        
        結果 = {"天": self.天場, "地": self.地場, "人": self.人場}
        self._歷史記錄.append(結果)
        return 結果
    
    def 獲取流場方向(self, x: float, y: float) -> Tuple[float, float]:
        """計算流場方向向量"""
        # 基於三才權重的流場計算
        天影響 = math.sin(x * 0.01 + self.天場 * math.pi)
        地影響 = math.cos(y * 0.01 + self.地場 * math.pi)
        人影響 = (self.人場 - 0.5) * 2
        
        dx = 天影響 * self.天場 + 地影響 * self.人場
        dy = 地影響 * self.地場 + 天影響 * self.人場
        
        # 歸一化
        長度 = math.sqrt(dx*dx + dy*dy)
        if 長度 > 0:
            dx /= 長度
            dy /= 長度
        
        return dx, dy


