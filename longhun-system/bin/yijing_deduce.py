# 节气加权系数
JIEQI_WEIGHT = {
    "立春":+3,"雨水":+3,"惊蛰":+3,"春分":+3,"清明":+3,"谷雨":+3,
    "立夏": 0,"小满": 0,"芒种": 0,"夏至": 0,"小暑": 0,"大暑": 0,
    "立秋":-2,"处暑":-2,"白露":-2,"秋分":-2,"寒露":-2,"霜降":-2,
    "立冬":+1,"小雪":+1,"大雪":+1,"冬至":+1,"小寒":+1,"大寒":+1,
}

GUA_BY_NO = {g[0]: g for g in HEXAGRAMS}
GUA_BY_NAME = {g[2]: g for g in HEXAGRAMS}


# ── 核心函数 ─────────────────────────────────────────────────

def 取当前卦(dr: int, 五行: str) -> dict:
    """根据数字根+五行定位当前卦象"""
    candidates = WUXING_TO_GUA.get(五行, [1])
    idx = dr % len(candidates)
    gua_no = candidates[idx]
    g = GUA_BY_NO[gua_no]
    return {"序号":g[0],"符号":g[1],"卦名":g[2],"卦辞":g[5],"策略":g[6],"五行":g[7],"三色":g[8]}


def 推演时间线(目标: str, 起始年: int, 结束年: int, 当前节气: str = "春分") -> list:
    """
    按季度推演时间线，每个节点给出卦象+三色+行动建议
    """
    节气权重 = JIEQI_WEIGHT.get(当前节气, 0)
    timeline = []
    seasons = ["Q1·春", "Q2·夏", "Q3·秋", "Q4·冬"]
    season_weight = [+3, 0, -2, +1]

    # 目标关键词 → 推荐卦象
    目标卦MAP = {
        "专利": [26,9,53,11],   # 大畜·小畜·渐·泰
        "项目": [1,16,34,14],   # 乾·豫·大壮·大有
        "合作": [13,8,45,58],   # 同人·比·萃·兑
        "发布": [55,35,14,49],  # 丰·晋·大有·革
        "审计": [21,15,48,29],  # 噬嗑·谦·井·坎
        "部署": [46,53,32,11],  # 升·渐·恒·泰
    }
    gua_sequence = 目标卦MAP.get(目标, [1,53,32,11])

    for year in range(起始年, 结束年 + 1):
        for qi, (season, sw) in enumerate(zip(seasons, season_weight)):
            total_weight = sw + (节气权重 if qi == 0 else 0)
            gua_idx = (year - 起始年) * 4 + qi
            gua = GUA_BY_NO.get(gua_sequence[gua_idx % len(gua_sequence)])

            if total_weight >= 2:
                三色 = "🟢"
                行动 = f"可推进：{gua[6]}"
            elif total_weight >= 0:
                三色 = "🟡"
                行动 = f"谨慎推进：{gua[5]}"
            else:
                三色 = "🔴"
                行动 = f"暂缓：蓄势待机，{gua[6]}"

            timeline.append({
                "节点": f"{year}-{season}",
                "卦象": f"{gua[1]}{gua[2]}",
                "卦辞": gua[5],
                "节气权重": total_weight,
                "三色": 三色,
                "行动": 行动,
            })
    return timeline


def 推演简报(目标: str, 起始年: int, 结束年: int, 当前节气: str,
              dr: int = 0, 五行: str = "土") -> str:
    """生成推演简报，供 system prompt 或用户直接阅读"""
    当前卦 = 取当前卦(dr, 五行)
    timeline = 推演时间线(目标, 起始年, 结束年, 当前节气)

    绿 = [t for t in timeline if t["三色"] == "🟢"]
    黄 = [t for t in timeline if t["三色"] == "🟡"]
    红 = [t for t in timeline if t["三色"] == "🔴"]

    lines = [
        f"【易经推演 · {目标} · {起始年}-{结束年}】",
        f"当前卦象：{当前卦['符号']}{当前卦['卦名']} · {当前卦['策略']}",
        f"节气：{当前节气}（权重{JIEQI_WEIGHT.get(当前节气,0):+d}）",
        f"🟢可推进 {len(绿)}个节点 | 🟡谨慎 {len(黄)}个 | 🔴暂缓 {len(红)}个",
        "",
    ]
    for t in timeline[:8]:  # 前8个节点详情
        lines.append(f"{t['三色']} {t['节点']} {t['卦象']} — {t['行动']}")

    if len(timeline) > 8:
        lines.append(f"… 共{len(timeline)}个节点，已展示前8个")

    # 校验规则：不用"必然"，用"更可能"
    最佳节点 = 绿[0] if 绿 else (黄[0] if 黄 else 红[0])
    lines.append(f"\n最佳起点：{最佳节点['节点']} — 条件：{当前节气}加权+目标{目标}匹配")
    lines.append("（注：以上为概率推演，结论基于节气/五行/卦象，非绝对预测）")

    return "\n".join(lines)


def 检测推演触发(user_text: str) -> dict | None:
    """检测用户输入是否触发易经推演，返回推演参数或None"""
    触发词 = ["推演", "卦", "易经", "时机", "几年", "几时", "什么时候合适", "推算"]
    if not any(w in user_text for w in 触发词):
        return None

    # 简单提取目标
    目标关键词 = {"专利": "专利", "项目": "项目", "合作": "合作",
                   "发布": "发布", "审计": "审计", "部署": "部署"}
    目标 = "项目"
    for k, v in 目标关键词.items():
        if k in user_text:
            目标 = v
            break

    now = datetime.now()
    return {
        "目标": 目标,
        "起始年": now.year,
        "结束年": now.year + 3,
    }


