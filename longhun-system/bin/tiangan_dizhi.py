def 今日节日(dt: datetime = None) -> str:
    """查今日是否有节日"""
    if dt is None:
        dt = datetime.now()
    key = (dt.month, dt.day)
    found = []
    if key in SOLAR_HOLIDAYS:
        found.append(SOLAR_HOLIDAYS[key])
    year_str = str(dt.year)
    if year_str in LUNAR_HOLIDAYS_APPROX:
        if key in LUNAR_HOLIDAYS_APPROX[year_str]:
            found.append(LUNAR_HOLIDAYS_APPROX[year_str][key])
    return "·".join(found) if found else ""


