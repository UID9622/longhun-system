def _太阳黄经(dt: datetime) -> float:
    """近似计算太阳黄经（度），精度约±1°"""
    # 儒略日
    a = (14 - dt.month) // 12
    y = dt.year + 4800 - a
    m = dt.month + 12 * a - 3
    jdn = dt.day + (153 * m + 2) // 5 + 365 * y + y // 4 - y // 100 + y // 400 - 32045
    jd = jdn - 0.5 + dt.hour / 24.0

    # 简化VSOP87
    n = jd - 2451545.0
    L = (280.460 + 0.9856474 * n) % 360
    g = math.radians((357.528 + 0.9856003 * n) % 360)
    lam = math.radians(L + 1.915 * math.sin(g) + 0.020 * math.sin(2 * g))
    return math.degrees(lam) % 360


def 当前节气(dt: datetime = None) -> dict:
    """返回最近节气（已过）和下一节气"""
    if dt is None:
        dt = datetime.now()
    lon = _太阳黄经(dt)

    # 找上一节气和下一节气
    sorted_jq = sorted(JIEQI_LIST, key=lambda x: x[0])
    prev_name, next_name = sorted_jq[-1][1], sorted_jq[0][1]
    prev_lon, next_lon = sorted_jq[-1][0], sorted_jq[0][0]

    for deg, name in sorted_jq:
        if lon >= deg:
            prev_lon, prev_name = deg, name
        else:
            next_lon, next_name = deg, name
            break

    # 距下一节气剩余度数 → 大约天数（1°≈1.014天）
    delta = (next_lon - lon) % 360
    days_left = round(delta / 0.9856474)

    return {
        "当前节气": prev_name,
        "下一节气": next_name,
        "距下一节气": f"{days_left}天",
        "太阳黄经": f"{lon:.1f}°",
    }


