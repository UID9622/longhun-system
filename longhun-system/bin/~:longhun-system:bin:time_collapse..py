import random
import math
from typing import List, Dict, Tuple
from dataclasses import dataclass, field
from datetime import datetime
import hashlib


# ============================================================
# 数据模型
# ============================================================

@dataclass
class 时间推演:
    """当前卦象/状态（输入）"""
    熵值: float          # 0~1，越低越确定，越高越混沌
    卦象名称: str = "未定义"
    时间戳: str = field(default_factory=lambda: datetime.now().isoformat())
    
    def __post_init__(self):
        self.熵值 = max(0.0, min(1.0, self.熵值))


@dataclass
class 演化路径:
    """单条蒙特卡洛路径"""
    最终熵值: float
    最终行动: str
    概率: float
    路径ID: str = field(default_factory=lambda: hashlib.md5(str(random.random()).encode()).hexdigest()[:8])


@dataclass
class 折叠结果:
    """折叠后的分支结果"""
    行动: str           # 该分支建议的行动
    概率: float         # 该分支的概率
    平均熵值: float     # 该分支的平均熵
    路径数: int         # 该分支包含的路径数量
    置信区间: Tuple[float, float] = (0.0, 0.0)  # 熵的95%置信区间


@dataclass
class 坍缩结果:
    """观测锁定后的结果"""
    坍缩行动: str           # 观察后确定的行动
    坍缩熵值: float         # 坍缩后的熵值
    置信度: float           # 这个坍缩有多可信（0~1）
    所有分支: List[折叠结果]  # 其他可能分支（用于审计）
    DNA追溯码: str = field(default_factory=lambda: "")
    坍缩时间: str = field(default_factory=lambda: datetime.now().isoformat())


# ============================================================
# 核心引擎
# ============================================================

class 时间坍缩引擎:
    """
    时间坍缩引擎
    折叠 + 坍缩 双阶段算法
    """
    
    # 基础加速倍数：物理层100 × 算法层500 × 智慧层8.3 = 415,000倍
    基础加速: float = 415_000
    
    # 蒙特卡洛默认路径数
    默认路径数: int = 10000
    
    # 熵值阈值（用于行动决策）
    熵阈值_观察: float = 0.7   # >0.7 观察等待
    熵阈值_调整: float = 0.4   # >0.4 调整优化
    # <=0.4 执行推进
    
    def __init__(self, 路径数: int = None):
        self.路径数 = 路径数 or self.默认路径数
        self._random_state = random.Random()
    
    def 折叠(self, 未来年数: int, 当前卦象: 时间推演) -> List[折叠结果]:
        """
        折叠：将长时间演化压缩到瞬间计算
        
        参数:
            未来年数: 要推演的年数
            当前卦象: 当前状态（含熵值）
        
        返回:
            折叠结果列表，按概率降序排列
        """
        # 每1秒推演 = 基础加速 × 年数
        推演秒数 = 未来年数 / self.基础加速
        
        # 蒙特卡洛模拟：生成N条可能的演化路径
        路径列表 = []
        
        for _ in range(self.路径数):
            # 熵值随机游走（受当前熵值影响，标准差与当前熵值正相关）
            漂移标准差 = 当前卦象.熵值 * 0.15 + 0.05
            漂移 = self._random_state.gauss(0, 漂移标准差)
            
            新熵值 = 当前卦象.熵值 + 漂移
            新熵值 = max(0.0, min(1.0, 新熵值))  # 限制在0~1
            
            # 根据新熵值决定行动
            if 新熵值 > self.熵阈值_观察:
                行动 = "观察等待"
            elif 新熵值 > self.熵阈值_调整:
                行动 = "调整优化"
            else:
                行动 = "执行推进"
            
            路径列表.append(演化路径(
                最终熵值=新熵值,
                最终行动=行动,
                概率=1.0 / self.路径数  # 均匀先验
            ))
        
        # 按行动聚类
        聚类: Dict[str, List[演化路径]] = {}
        for 路径 in 路径列表:
            if 路径.最终行动 not in 聚类:
                聚类[路径.最终行动] = []
            聚类[路径.最终行动].append(路径)
        
        # 生成折叠结果
        结果 = []
        for 行动, 路径组 in 聚类.items():
            熵值列表 = [p.最终熵值 for p in 路径组]
            平均熵 = sum(熵值列表) / len(熵值列表)
            总概率 = sum(p.概率 for p in 路径组)
            
            # 计算95%置信区间（假设正态分布）
            置信区间 = self._计算置信区间(熵值列表)
            
            结果.append(折叠结果(
                行动=行动,
                概率=总概率,
                平均熵值=平均熵,
                路径数=len(路径组),
                置信区间=置信区间
            ))
        
        # 按概率降序排列
        结果.sort(key=lambda x: x.概率, reverse=True)
        return 结果
    
    def 坍缩(self, 未来年数: int, 当前推演: 时间推演) -> 坍缩结果:
        """
        坍缩：观测并确定"当前最可能的未来"
        
        参数:
            未来年数: 要推演的年数
            当前推演: 当前状态
        
        返回:
            坍缩结果（锁定后的行动）
        """
        所有分支 = self.折叠(未来年数, 当前推演)
        
        if not 所有分支:
            return 坍缩结果(
                坍缩行动="无法坍缩",
                坍缩熵值=0.5,
                置信度=0.0,
                所有分支=[]
            )
        
        # 坍缩到最可能的分支
        最可能分支 = 所有分支[0]
        
        # 生成DNA追溯码
        dna = self._生成DNA(未来年数, 当前推演, 最可能分支)
        
        return 坍缩结果(
            坍缩行动=最可能分支.行动,
            坍缩熵值=最可能分支.平均熵值,
            置信度=最可能分支.概率,
            所有分支=所有分支,
            DNA追溯码=dna
        )
    
    def 批量坍缩(self, 未来年数列表: List[int], 当前推演: 时间推演) -> List[坍缩结果]:
        """批量坍缩多个时间点"""
        return [self.坍缩(n, 当前推演) for n in 未来年数列表]
    
    def 熵趋势分析(self, 未来年数: int, 当前推演: 时间推演, 步数: int = 10) -> Dict:
        """
        分析熵值随时间的演化趋势
        
        参数:
            未来年数: 总年数
            当前推演: 当前状态
            步数: 分成多少步分析
        
        返回:
            趋势分析结果
        """
        步长 = 未来年数 / 步数
        熵值序列 = []
        
        当前熵 = 当前推演.熵值
        for i in range(1, 步数 + 1):
            分支 = self.折叠(i * 步长, 时间推演(熵值=当前熵))
            if 分支:
                加权熵 = sum(b.平均熵值 * b.概率 for b in 分支)
                熵值序列.append(加权熵)
                # 更新当前熵为加权熵（用于下一步）
                当前熵 = 加权熵
        
        return {
            "年数点": [i * 步长 for i in range(1, 步数 + 1)],
            "熵值序列": 熵值序列,
            "起始熵": 当前推演.熵值,
            "终点熵": 熵值序列[-1] if 熵值序列 else 当前推演.熵值,
            "趋势": "上升" if 熵值序列 and 熵值序列[-1] > 当前推演.熵值 else "下降" if 熵值序列 and 熵值序列[-1] < 当前推演.熵值 else "稳定"
        }
    
    # ========== 私有方法 ==========
    
    def _计算置信区间(self, 数值列表: List[float], 置信度: float = 0.95) -> Tuple[float, float]:
        """计算95%置信区间"""
        if len(数值列表) < 2:
            return (0.0, 0.0)
        
        mean = sum(数值列表) / len(数值列表)
        variance = sum((x - mean) ** 2 for x in 数值列表) / len(数值列表)
        std = math.sqrt(variance)
        
        # Z值对应95%置信度 ≈ 1.96
        z = 1.96
        margin = z * std / math.sqrt(len(数值列表))
        
        return (max(0.0, mean - margin), min(1.0, mean + margin))
    
    def _生成DNA(self, 未来年数: int, 当前推演: 时间推演, 最可能分支: 折叠结果) -> str:
        """生成DNA追溯码"""
        data = f"{未来年数}|{当前推演.熵值}|{最可能分支.行动}|{最可能分支.概率}|{datetime.now().isoformat()}"
        hash_part = hashlib.sha256(data.encode()).hexdigest()[:24]
        return f"#时间坍缩DNA⚡️{datetime.now().strftime('%Y%m%d')}-{hash_part}"


# ============================================================
# 便捷函数（与Swift版本API对齐）
# ============================================================

def 折叠(未来年数: int, 当前熵值: float, 路径数: int = 10000) -> List[折叠结果]:
    """便捷函数：折叠"""
    引擎 = 时间坍缩引擎(路径数=路径数)
    return 引擎.折叠(未来年数, 时间推演(熵值=当前熵值))


def 坍缩(未来年数: int, 当前熵值: float, 路径数: int = 10000) -> 坍缩结果:
    """便捷函数：坍缩"""
    引擎 = 时间坍缩引擎(路径数=路径数)
    return 引擎.坍缩(未来年数, 时间推演(熵值=当前熵值))


