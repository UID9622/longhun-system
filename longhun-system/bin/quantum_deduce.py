# 洛书矩阵（三阶幻方，每行/列/对角线之和=15）
LUOSHU = [
    [4, 9, 2],
    [3, 5, 7],
    [8, 1, 6],
]

# 五行 → 洛书向量（基向量）
WUXING_VECTOR = {
    "木": [3, 8, 0],  # 3/8=木
    "火": [2, 7, 0],  # 2/7=火
    "土": [5, 0, 0],  # 5=土
    "金": [4, 9, 0],  # 4/9=金
    "水": [1, 6, 0],  # 1/6=水
}

# 量子态描述
QUANTUM_STATES = {
    (0, 0, 0): ("归零态", "万象归一，静待时机", "🔵"),
    (3, 3, 3): ("共振态", "三才同频，行动有力", "🟢"),
    (6, 6, 6): ("反共振", "能量过满，宜收敛", "🟡"),
    (9, 9, 9): ("饱和态", "临界点，需转化", "🔴"),
}


def 矩阵迭代(向量: list, 次数: int = 3) -> list:
    """洛书矩阵迭代 v % 9"""
    v = list(向量)
    for _ in range(次数):
        new_v = [
            (LUOSHU[0][0]*v[0] + LUOSHU[0][1]*v[1] + LUOSHU[0][2]*v[2]) % 9,
            (LUOSHU[1][0]*v[0] + LUOSHU[1][1]*v[1] + LUOSHU[1][2]*v[2]) % 9,
            (LUOSHU[2][0]*v[0] + LUOSHU[2][1]*v[1] + LUOSHU[2][2]*v[2]) % 9,
        ]
        v = new_v
    return v


def 量子纠缠(dr: int, 日五行: str, 情绪: str = "平静") -> dict:
    """
    输入三个维度，计算量子纠缠态
    dr → 数字根向量
    日五行 → 洛书基向量
    情绪 → 扰动因子
    """
    # 数字根向量
    dr_向量 = [dr, (dr * 3) % 9, (dr * 6) % 9]

    # 五行基向量
    五行向量 = WUXING_VECTOR.get(日五行, [5, 0, 0])

    # 情绪扰动（简单映射）
    情绪扰动 = {
        "顿悟": 1, "开心": 2, "平静": 0,
        "焦虑": -1, "愤怒": -2, "崩溃": -3,
    }.get(情绪, 0)

    # 合并向量（纠缠）
    纠缠向量 = [
        (dr_向量[i] + 五行向量[i] + 情绪扰动) % 9
        for i in range(3)
    ]

    # 洛书迭代3次
    结果向量 = 矩阵迭代(纠缠向量, 次数=3)

    # 识别量子态
    态键 = tuple(结果向量)
    if 态键 in QUANTUM_STATES:
        态名, 态说明, 态色 = QUANTUM_STATES[态键]
    else:
        # 计算向量和，判断趋势
        向量和 = sum(结果向量)
        if 向量和 <= 5:
            态名, 态说明, 态色 = "收缩态", "能量内敛，适合深思", "🔵"
        elif 向量和 <= 12:
            态名, 态说明, 态色 = "平衡态", "阴阳和谐，顺势而为", "🟢"
        elif 向量和 <= 18:
            态名, 态说明, 态色 = "扩张态", "气场外放，适合行动", "🟡"
        else:
            态名, 态说明, 态色 = "耗散态", "能量过散，需聚焦", "🔴"

    # 推演结论
    推演 = f"dr={dr}×{日五行}×{情绪} → 纠缠{纠缠向量} → 迭代{结果向量} → {态名}"

    return {
        "dr向量": dr_向量,
        "五行向量": 五行向量,
        "纠缠向量": 纠缠向量,
        "结果向量": 结果向量,
        "量子态": 态名,
        "态色": 态色,
        "说明": 态说明,
        "推演": 推演,
        "简报": f"【量子纠缠】{态色}{态名} · {态说明}",
    }


