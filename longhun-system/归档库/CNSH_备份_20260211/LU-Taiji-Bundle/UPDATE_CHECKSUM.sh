#!/bin/bash
# ============================================================================
# LU-Taiji Bundle 校验和更新脚本
# ============================================================================
# 功能：为所有新增文件生成校验和，验证文件完整性
# DNA: #ZHUGEXIN⚡️2025-01-27-LU-TAIJI-CHECKSUM-UPDATE-v2.1
# 确认码: #CONFIRM🌌9622-ONLY-ONCE🧬LK9X-772Z
# ============================================================================

set -e  # 遇到错误立即退出

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BUNDLE_ROOT="$SCRIPT_DIR"

# ============================================================================
# ANSI 颜色定义
# ============================================================================
GREEN='\033[0;32m'   # 绿色 - 成功
RED='\033[0;31m'     # 红色 - 错误
YELLOW='\033[0;33m' # 黄色 - 警告
BLUE='\033[0;34m'    # 蓝色 - 信息
NC='\033[0m'         # 重置颜色

# ============================================================================
# 显示横幅
# ============================================================================
echo -e "${BLUE}═══════════════════════════════════════${NC}"
echo -e "${BLUE}  LU-Taiji Bundle 校验和更新${NC}"
echo -e "${BLUE}═══════════════════════════════════════${NC}\n"

# ============================================================================
# 步骤 1: 设置脚本权限
# ============================================================================
echo -e "${YELLOW}[1/3] 正在设置脚本权限...${NC}"
chmod +x "$BUNDLE_ROOT/scripts/"*.sh
chmod +x "$BUNDLE_ROOT/scripts/"*.mjs
chmod +x "$BUNDLE_ROOT/devices/"*/*.sh
chmod +x "$BUNDLE_ROOT/"*.sh
echo -e "${GREEN}✓ 权限设置完成${NC}"

# ============================================================================
# 步骤 2: 生成校验和
# ============================================================================
echo -e "\n${YELLOW}[2/3] 正在生成校验和...${NC}"
bash "$BUNDLE_ROOT/scripts/checksum.sh"

# ============================================================================
# 步骤 3: 验证文件完整性
# ============================================================================
echo -e "\n${YELLOW}[3/3] 正在验证文件完整性...${NC}"
if [ -f "$BUNDLE_ROOT/scripts/validate_schema.mjs" ]; then
    if node "$BUNDLE_ROOT/scripts/validate_schema.mjs"; then
        echo -e "${GREEN}✓ 验证通过${NC}"
    else
        echo -e "${RED}✗ 验证失败${NC}"
        exit 1
    fi
else
    echo -e "${YELLOW}⚠️  验证脚本未找到${NC}"
fi

# ============================================================================
# 完成
# ============================================================================
echo -e "\n${BLUE}═══════════════════════════════════════${NC}"
echo -e "${GREEN}  ✓ 校验和更新完成！${NC}"
echo -e "${BLUE}═══════════════════════════════════════${NC}"
