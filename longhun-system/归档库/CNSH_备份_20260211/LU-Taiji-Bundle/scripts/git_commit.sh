#!/bin/bash
# ============================================================================
# LU-Taiji Bundle Git 提交脚本
# ============================================================================
# 功能：将 LU-Taiji Bundle 提交到 Git 仓库并推送
# DNA: #ZHUGEXIN⚡️2025-01-27-LU-TAIJI-GIT-v2.1
# 确认码: #CONFIRM🌌9622-ONLY-ONCE🧬LK9X-772Z
# ============================================================================

set -e  # 遇到错误立即退出

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BUNDLE_ROOT="$(dirname "$SCRIPT_DIR")"

# ============================================================================
# ANSI 颜色定义
# ============================================================================
GREEN='\033[0;32m'   # 绿色 - 成功
RED='\033[0;31m'     # 红色 - 错误
YELLOW='\033[0;33m' # 黄色 - 警告
BLUE='\033[0;34m'    # 蓝色 - 信息
NC='\033[0m'         # 重置颜色

echo -e "${BLUE}═══════════════════════════════════════${NC}"
echo -e "${BLUE}  LU-Taiji Bundle Git 提交${NC}"
echo -e "${BLUE}═══════════════════════════════════════${NC}\n"

# ============================================================================
# 检查是否在 git 仓库中
# ============================================================================
if ! git rev-parse --git-dir > /dev/null 2>&1; then
    echo -e "${RED}✗ 错误: 不在 git 仓库中${NC}"
    echo -e "${YELLOW}  请在项目根目录运行此脚本${NC}"
    exit 1
fi

# ============================================================================
# 检查是否有变更
# ============================================================================
if git diff --quiet && git diff --cached --quiet; then
    echo -e "${YELLOW}⚠️  没有需要提交的变更${NC}"
    exit 0
fi

# ============================================================================
# 步骤 1: 添加文件
# ============================================================================
echo -e "${YELLOW}[1/3] 正在添加文件...${NC}"
git add "$BUNDLE_ROOT"

# ============================================================================
# 步骤 2: 提交
# ============================================================================
echo -e "${YELLOW}[2/3] 正在提交...${NC}"
git commit -m "feat: 添加 LU-Taiji Bundle v2.1

- 完整的内容模型和知识图谱
- Schema 验证脚本 (validate_schema.mjs)
- 校验和生成工具 (checksum.sh)
- Notion 导入功能 (import_notion.mjs)
- 快速启动脚本 (quickstart.sh)
- 安全审计报告 (100% 通过)
- 完整文档和 README

DNA: #ZHUGEXIN⚡️2025-01-27-LU-TAIJI-BUNDLE-v2.1
确认码: #CONFIRM🌌9622-ONLY-ONCE🧬LK9X-772Z"

# ============================================================================
# 步骤 3: 推送
# ============================================================================
echo -e "${YELLOW}[3/3] 正在推送...${NC}"
git push

echo -e "\n${BLUE}═══════════════════════════════════════${NC}"
echo -e "${GREEN}  ✓ 提交成功！${NC}"
echo -e "${BLUE}═══════════════════════════════════════${NC}"
